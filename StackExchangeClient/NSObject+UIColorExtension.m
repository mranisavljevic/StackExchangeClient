#import "NSObject+UIColorExtension.h"

@implementation UIColor (UIColorExtension)

+(UIColor *)pastelPinkColor {
    return [UIColor colorWithRed:1.000 green:0.898 blue:0.831 alpha:1.000];
}

+(UIColor *)pastelGreenColor {
    return [UIColor colorWithRed:0.949 green:1.000 blue:0.973 alpha:1.000];
}

+(UIColor *)pastelBlueColor {
    return [UIColor colorWithRed:0.706 green:0.937 blue:1.000 alpha:1.000];
}

+(UIColor *)pastelYellowColor {
    return [UIColor colorWithRed:0.973 green:1.000 blue:0.729 alpha:1.000];
}

@end
